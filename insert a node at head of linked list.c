/*
 * For your reference:
 *
 * SinglyLinkedListNode {
 *     int data;
 *     SinglyLinkedListNode* next;
 * };
 *
 */
SinglyLinkedListNode* insertNodeAtHead(SinglyLinkedListNode* llist, int data) {
    SinglyLinkedListNode* curr=(SinglyLinkedListNode*)malloc(sizeof(SinglyLinkedListNode));
    curr->data=data;
    curr->next=llist;
    llist=curr;
    return llist;
    


}

